const fs = require('fs');
const path = require('path');

const SENTENCES_DIR = path.join(__dirname, '..', 'data', 'sentences');
const OUTPUT_FILE = path.join(__dirname, '..', 'edge-functions', '[[default]].js');

const CATEGORIES = [
  { key: 'a', name: '动画' },
  { key: 'b', name: '漫画' },
  { key: 'c', name: '游戏' },
  { key: 'd', name: '文学' },
  { key: 'e', name: '原创' },
  { key: 'f', name: '网络' },
  { key: 'g', name: '其他' },
  { key: 'h', name: '影视' },
  { key: 'i', name: '诗词' },
  { key: 'j', name: '网易云' },
  { key: 'k', name: '哲学' },
  { key: 'l', name: '抖机灵' }
];

function buildEdgeFunction() {
  const allSentences = [];

  for (const category of CATEGORIES) {
    const filePath = path.join(SENTENCES_DIR, `${category.key}.json`);

    if (!fs.existsSync(filePath)) {
      console.warn(`Warning: ${filePath} not found`);
      continue;
    }

    const data = JSON.parse(fs.readFileSync(filePath, 'utf-8'));

    if (Array.isArray(data)) {
      for (const item of data) {
        if (item && item.hitokoto) {
          allSentences.push({
            id: item.uuid || String(item.id),
            hitokoto: item.hitokoto,
            type: item.type || category.key,
            from: item.from || '',
            from_who: item.from_who || '',
            creator: item.creator || 'hitokoto',
            creator_uid: item.creator_uid || 0,
            reviewer: item.reviewer || 0,
            uuid: item.uuid || String(item.id),
            created_at: item.created_at || ''
          });
        }
      }
    }
  }

  // 生成完整的 Edge Function 文件，数据直接嵌入为常量
  const functionContent = `// Hitokoto API - Edge Function
// Auto-generated by build-edge-function.js
// Do not edit manually

// 句子数据（构建时嵌入）
const SENTENCES = ${JSON.stringify(allSentences)};

// 分类数据（构建时嵌入）
const CATEGORIES = ${JSON.stringify(CATEGORIES)};

function getRandomSentence(sentences, categories, minLength, maxLength) {
  let filtered = sentences;

  if (categories.length > 0) {
    filtered = filtered.filter(function(s) {
      return categories.indexOf(s.type) !== -1;
    });
  }

  if (minLength > 0 || maxLength < 10000) {
    filtered = filtered.filter(function(s) {
      const len = s.hitokoto.length;
      return len >= minLength && len <= maxLength;
    });
  }

  if (filtered.length === 0) return null;

  return filtered[Math.floor(Math.random() * filtered.length)];
}

function parseQueryParams(url) {
  const params = new URLSearchParams(url.search);

  const c = params.get('c');
  const categories = c ? c.split('') : [];
  const encode = params.get('encode') || 'json';
  let minLength = parseInt(params.get('min_length') || '0', 10);
  let maxLength = parseInt(params.get('max_length') || '10000', 10);
  const charset = params.get('charset') || 'utf-8';
  const callback = params.get('callback') || '';

  if (isNaN(minLength)) minLength = 0;
  if (isNaN(maxLength)) maxLength = 10000;

  if (maxLength < minLength) {
    const tmp = minLength;
    minLength = maxLength;
    maxLength = tmp;
  }

  return { categories, encode, minLength, maxLength, charset, callback };
}

function createResponse(data, encode, callback, charset) {
  let body;
  let contentType;

  if (encode === 'text') {
    body = data.hitokoto;
    contentType = 'text/plain; charset=' + charset;
  } else if (encode === 'js') {
    body = (callback || 'hitokoto') + '(' + JSON.stringify(data) + ');';
    contentType = 'application/javascript; charset=' + charset;
  } else {
    body = JSON.stringify(data);
    contentType = 'application/json; charset=' + charset;
  }

  return new Response(body, {
    headers: {
      'Content-Type': contentType,
      'Access-Control-Allow-Origin': '*',
      'Access-Control-Allow-Methods': 'GET, HEAD, OPTIONS'
    }
  });
}

function createErrorResponse(status, message) {
  return new Response(JSON.stringify({
    status: status,
    message: message,
    data: null,
    ts: Date.now()
  }), {
    status: status,
    headers: {
      'Content-Type': 'application/json; charset=utf-8',
      'Access-Control-Allow-Origin': '*'
    }
  });
}

export default async function onRequest(context) {
  try {
    const request = context.request;
    const url = new URL(request.url);

    if (request.method === 'OPTIONS') {
      return new Response(null, {
        status: 204,
        headers: {
          'Access-Control-Allow-Origin': '*',
          'Access-Control-Allow-Methods': 'GET, HEAD, OPTIONS',
          'Access-Control-Allow-Headers': 'Content-Type'
        }
      });
    }

    if (request.method !== 'GET' && request.method !== 'HEAD') {
      return createErrorResponse(405, 'Method Not Allowed');
    }

    if (url.pathname === '/ping') {
      return new Response(JSON.stringify({
        status: 200,
        message: 'ok',
        data: {},
        ts: Date.now()
      }), {
        headers: {
          'Content-Type': 'application/json; charset=utf-8',
          'Access-Control-Allow-Origin': '*'
        }
      });
    }

    if (url.pathname === '/status') {
      return new Response(JSON.stringify({
        name: 'hitokoto-api',
        version: '2.0.0-edge',
        message: 'Love us? donate at https://hitokoto.cn/donate',
        website: 'https://hitokoto.cn',
        server_id: 'edge-one',
        server_status: {
          categories: CATEGORIES.length,
          total: SENTENCES.length
        },
        copyright: 'MoeCraft © ' + new Date().getFullYear() + ' All Rights Reserved.',
        now: new Date().toString(),
        ts: Date.now()
      }), {
        headers: {
          'Content-Type': 'application/json; charset=utf-8',
          'Access-Control-Allow-Origin': '*'
        }
      });
    }

    const params = parseQueryParams(url);
    const sentence = getRandomSentence(
      SENTENCES,
      params.categories,
      params.minLength,
      params.maxLength
    );

    if (!sentence) {
      return createErrorResponse(404, 'No matching sentences found');
    }

    return createResponse(sentence, params.encode, params.callback, params.charset);

  } catch (error) {
    return createErrorResponse(500, 'Internal Server Error');
  }
}
`;

  fs.writeFileSync(OUTPUT_FILE, functionContent, 'utf-8');
  console.log(`Generated ${OUTPUT_FILE} with ${allSentences.length} sentences`);
}

buildEdgeFunction();
