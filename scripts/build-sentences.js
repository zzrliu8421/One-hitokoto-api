const fs = require('fs');
const path = require('path');

const SENTENCES_DIR = path.join(__dirname, '..', 'data', 'sentences');
const OUTPUT_FILE = path.join(__dirname, '..', 'edge-functions', 'sentences-data.js');

const CATEGORIES = [
  { key: 'a', name: '动画' },
  { key: 'b', name: '漫画' },
  { key: 'c', name: '游戏' },
  { key: 'd', name: '文学' },
  { key: 'e', name: '原创' },
  { key: 'f', name: '网络' },
  { key: 'g', name: '其他' },
  { key: 'h', name: '影视' },
  { key: 'i', name: '诗词' },
  { key: 'j', name: '网易云' },
  { key: 'k', name: '哲学' },
  { key: 'l', name: '抖机灵' }
];

function buildSentencesModule() {
  const allSentences = [];

  for (const category of CATEGORIES) {
    const filePath = path.join(SENTENCES_DIR, `${category.key}.json`);
    
    if (!fs.existsSync(filePath)) {
      console.warn(`Warning: ${filePath} not found`);
      continue;
    }

    const data = JSON.parse(fs.readFileSync(filePath, 'utf-8'));
    
    if (Array.isArray(data)) {
      for (const item of data) {
        if (item && item.hitokoto) {
          allSentences.push({
            id: item.uuid || String(item.id),
            hitokoto: item.hitokoto,
            type: item.type || category.key,
            from: item.from || '',
            from_who: item.from_who || '',
            creator: item.creator || 'hitokoto',
            creator_uid: item.creator_uid || 0,
            reviewer: item.reviewer || 0,
            uuid: item.uuid || String(item.id),
            created_at: item.created_at || ''
          });
        }
      }
    }
  }

  // 生成 JavaScript 模块
  const moduleContent = `// Auto-generated by build-sentences.js
// Do not edit manually

export const SENTENCES = ${JSON.stringify(allSentences)};

export const CATEGORIES = ${JSON.stringify(CATEGORIES)};
`;

  fs.writeFileSync(OUTPUT_FILE, moduleContent, 'utf-8');
  console.log(`Generated ${OUTPUT_FILE} with ${allSentences.length} sentences`);
}

buildSentencesModule();
