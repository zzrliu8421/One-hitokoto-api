import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const CATEGORIES = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l'];
const BASE_URL = 'https://raw.githubusercontent.com/hitokoto-osc/sentences-bundle/master/sentences';

async function fetchJSON(url) {
  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`HTTP ${response.status}: ${url}`);
  }
  return response.json();
}

async function main() {
  const projectRoot = path.resolve(__dirname, '..');
  const outputDir = path.join(projectRoot, 'edge-functions', '_data');
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  const allSentences = {};
  const categoryStats = {};

  for (const cat of CATEGORIES) {
    const url = `${BASE_URL}/${cat}.json`;
    console.log(`Fetching ${url}...`);
    const sentences = await fetchJSON(url);
    allSentences[cat] = sentences;
    categoryStats[cat] = sentences.length;
    console.log(`  Loaded ${sentences.length} sentences for category "${cat}"`);
  }

  const totalSentences = Object.values(allSentences).reduce((sum, arr) => sum + arr.length, 0);
  console.log(`\nTotal sentences: ${totalSentences}`);
  console.log('Category breakdown:', JSON.stringify(categoryStats, null, 2));

  const jsContent = `// Auto-generated by scripts/fetch-sentences.js
// Total sentences: ${totalSentences}
// Categories: ${JSON.stringify(categoryStats)}
const sentences = ${JSON.stringify(allSentences)};
export default sentences;
`;
  const dataPath = path.join(outputDir, 'sentences.js');
  fs.writeFileSync(dataPath, jsContent, 'utf-8');
  console.log(`\nSaved bundled data to ${dataPath}`);
}

main().catch((err) => {
  console.error('Failed to fetch sentences:', err);
  process.exit(1);
});